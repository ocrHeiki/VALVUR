logger = utils.setup_logging("AJAJOON")

def main():
    print(LOGO)
    out_dir = utils.get_output_dir()
    out_file = os.path.join(out_dir, '51_tulemus_unified_timeline.csv')
    all_events = []
    csv_files = [f for f in os.listdir(out_dir) if f.startswith('raw_eksport_') and f.endswith('.csv')]
    for csv_file in csv_files:
        file_path = os.path.join(out_dir, csv_file)
        try:
            with open(file_path, 'r', encoding='utf-8') as f:
                reader = csv.DictReader(f)
                for row in reader:
                    row['_allikas'] = csv_file
                    all_events.append(row)
        except Exception as e:
            logger.error(f"CSV lugemise viga {csv_file}: {e}")
    all_events.sort(key=lambda r: r.get('TimeCreated', ''))
    if all_events:
        fieldnames = list(all_events[0].keys())
        with open(out_file, 'w', newline='', encoding='utf-8') as f:
            writer = csv.DictWriter(f, fieldnames=fieldnames)
            writer.writeheader()
            writer.writerows(all_events)
        logger.info(f"Koond ajajoon loodud: {out_file} ({len(all_events)} sündmust)")
    else:
        logger.warning("Ühtegi sündmust ei leitud CSV-failidest.")

if __name__ == "__main__":
    main()
